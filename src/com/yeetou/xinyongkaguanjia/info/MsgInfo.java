public class MsgInfo {

	private String raw_id;
	private String address;
	private Long received_tm;
	private String body;
	
	public String getRaw_id() {
		return raw_id;
	}
	public void setRaw_id(String raw_id) {
		this.raw_id = raw_id;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public Long getReceived_tm() {
		return received_tm;
	}
	public void setReceived_tm(Long received_tm) {
		this.received_tm = received_tm;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}





}
