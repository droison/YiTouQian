package com.yeetou.xinyongkaguanjia.info;

public class MonthPayments {

	private int year;
	private int month;
	private float income;
	private float expand;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public float getIncome() {
		return income;
	}

	public void setIncome(float income) {
		this.income = income;
	}

	public float getExpand() {
		return expand;
	}

	public void setExpand(float expand) {
		this.expand = expand;
	}

}
